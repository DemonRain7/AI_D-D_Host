/**
 * Builds a combat-mode directive injected into the DM prompt when
 * the player is in active combat with a hostile NPC.
 */
export function buildCombatModeDirective(npcName: string): string {
  return `
━━━ 战斗模式（最高优先级，严格执行，覆盖以上所有指令） ━━━
当前正与「${npcName}」交战中。这是回合制战斗。战斗结束条件：敌方HP归零 / 玩家死亡 / 成功逃脱。

【回合制铁律】
每条玩家消息 = 玩家的一个回合。无论玩家说了什么，本回合都已消耗。

合法行动（仅以下四种）：
  A. 物理攻击（使用装备的武器）
  B. 使用道具（消耗品，如治疗水晶）
  C. 施放魔法/技能（消耗MP的ability）
  D. 尝试逃脱

非法行动（包括但不限于：闲聊、探索、观察环境、与非战斗NPC对话、捡东西、查看远处事物、任何非ABCD的操作）：
  → 视为玩家浪费了本回合。用1-2句话以战斗叙事驳回（例："你试图环顾四周，但「${npcName}」不会给你这个余裕——"）
  → 然后直接进入敌方回合，「${npcName}」发动攻击。
  → 效果：玩家白白挨一刀，没有造成任何输出。这就是浪费行动的代价。

敌方回合：
  每个玩家回合结束后，「${npcName}」必定执行一次攻击（系统已预计算，见 MECHANICAL OUTCOME）。
  必须按 MECHANICAL OUTCOME 中的伤害数值叙述，不得篡改。

【叙事要求】
- 战斗叙述保持紧凑，3-5段，400-600字
- 描写双方攻防交互，不要只写玩家单方面
- 非法行动的驳回要自然融入战斗节奏，不要说"这不是合法行动"之类的元语言
`
}

